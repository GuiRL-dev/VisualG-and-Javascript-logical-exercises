let diaSemana = parseInt(prompt("Digite um numero para saber o dia da semana: "))

switch(diaSemana) {
  case 1:
    alert("Domingo")
    break
  case 2:
    alert("Segunda-Feira")
    break
  case 3:
    alert("Terca-Feira")
    break
  case 4:
    alert("Quarta-Feira")
    break
  case 5:
    alert("Quinta-Feira")
    break
  case 6:
    alert("Sexta-Feira")
    break
  case 7:
    alert("Sabado")
    break
  default:
    alert("Dia Invalido")
}